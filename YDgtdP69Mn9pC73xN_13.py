def check_borders(lst, row, col):
  for i in range(-1, 2):
    for j in range(-1, 2):
      try:
        if lst[row + i][col + j] != "#" and row + i >= 0 and col + j >= 0:
          lst[row + i][col + j] = lst[row + i][col + j] + 1
      except IndexError:
        pass
        
  return lst

