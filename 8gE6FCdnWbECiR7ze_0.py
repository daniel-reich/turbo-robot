def smith_type(n):
    if n in primes:
        return "Trivial Smith"
    if is_smith(n):
        if is_smith(n + 1) and n + 1 not in primes:
            return "Youngest Smith"
        if is_smith(n - 1) and n - 1 not in primes:
            return "Oldest Smith"
        return "Single Smith"
    return "Not a Smith"

